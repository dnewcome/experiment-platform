/**
 * Simulation write route.
 *
 * POST /simulation/write
 *
 * Accepts aggregated simulation results (variant, n, conversions) and writes
 * synthetic user-level rows to the simulation tables so the data can be
 * queried from the Warehouse Analysis tab.
 *
 * Table names vary by adapter:
 *   BigQuery   — experiment_platform_simulation.experiment_assignments
 *                experiment_platform_simulation.experiment_facts
 *   SQLite /
 *   Postgres   — sim_assignments, sim_facts
 *
 * BigQuery uses a single multi-row INSERT per table for efficiency.
 * SQLite/Postgres use individual parameterized inserts.
 *
 * Generated schema:
 *   assignments: run_id, seed, flag_key, user_id, variant, assigned_at
 *   facts:       run_id, seed, flag_key, user_id, variant, metric_name,
 *                value, event_at
 *
 * Only converting users get a facts row (value = 1). Non-converters are
 * represented by the absence of a facts row — the warehouse analysis LEFT JOIN
 * coalesces their contribution to 0.
 */

import db from '../db/index.js';

// Resolve simulation table names for the active adapter.
function simTables() {
  if (db.dialect === 'bigquery') {
    // Import the dataset name that was set during adapter initialisation.
    // Dynamic import is needed because the bigquery module uses top-level await.
    return null; // resolved lazily below
  }
  return { assignments: 'sim_assignments', facts: 'sim_facts' };
}

// Escape a string value for inline SQL (BigQuery bulk INSERT only).
const esc = v => String(v ?? '').replace(/\\/g, '\\\\').replace(/'/g, "\\'");

export default async function simulationRoutes(app) {

  // Lazily resolve the BigQuery simulation dataset name once on first request.
  let bqSimDataset = null;
  async function getSimDataset() {
    if (bqSimDataset) return bqSimDataset;
    const mod = await import('../db/bigquery.js');
    bqSimDataset = mod.simDatasetId;
    return bqSimDataset;
  }

  // POST /simulation/write
  // Body: { run_id, seed, flag_key, metric_name?, variants: [{variant, n, conversions}] }
  app.post('/simulation/write', async (req, reply) => {
    const {
      run_id,
      seed       = 0,
      flag_key   = 'simulation',
      metric_name = 'conversion',
      variants,
    } = req.body ?? {};

    if (!run_id)              return reply.code(400).send({ error: 'run_id is required' });
    if (!Array.isArray(variants) || !variants.length)
      return reply.code(400).send({ error: 'variants must be a non-empty array' });

    const now        = new Date();
    const assignedAt = now.toISOString().replace('T', ' ').slice(0, 19);
    const eventAt    = new Date(now.getTime() + 3_600_000) // +1 hour
      .toISOString().replace('T', ' ').slice(0, 19);

    // Build row arrays from aggregated variant data.
    // Converts: user IDs u-{variant}-000001 … u-{variant}-{n}
    // Only converters get a facts row.
    const assignRows = []; // [run_id, seed, flag_key, user_id, variant, assigned_at]
    const factRows   = []; // [run_id, seed, flag_key, user_id, variant, metric_name, 1, event_at]

    for (const v of variants) {
      const { variant, n, conversions } = v;
      for (let i = 0; i < n; i++) {
        const userId = `u-${variant}-${String(i).padStart(6, '0')}`;
        assignRows.push([run_id, seed, flag_key, userId, variant, assignedAt]);
        if (i < conversions) {
          factRows.push([run_id, seed, flag_key, userId, variant, metric_name, 1, eventAt]);
        }
      }
    }

    try {
      if (db.dialect === 'bigquery') {
        const ds = await getSimDataset();
        await bulkInsertBigQuery(
          `${ds}.experiment_assignments`,
          ['run_id', 'seed', 'flag_key', 'user_id', 'variant', 'assigned_at'],
          assignRows,
        );
        await bulkInsertBigQuery(
          `${ds}.experiment_facts`,
          ['run_id', 'seed', 'flag_key', 'user_id', 'variant', 'metric_name', 'value', 'event_at'],
          factRows,
        );
      } else {
        await paramInsertLoop('sim_assignments',
          ['run_id', 'seed', 'flag_key', 'user_id', 'variant', 'assigned_at'],
          assignRows);
        await paramInsertLoop('sim_facts',
          ['run_id', 'seed', 'flag_key', 'user_id', 'variant', 'metric_name', 'value', 'event_at'],
          factRows);
      }
    } catch (err) {
      return reply.code(500).send({ error: err.message });
    }

    return {
      ok:          true,
      run_id,
      assignments: assignRows.length,
      facts:       factRows.length,
    };
  });
}

// ---------------------------------------------------------------------------
// Bulk insert helpers
// ---------------------------------------------------------------------------

// BigQuery: single INSERT ... VALUES (r1),(r2),... per table.
// Values are inlined as literals — all data is generated by our code so
// SQL injection risk is limited to flag_key/variant names, which are escaped.
async function bulkInsertBigQuery(table, cols, rows) {
  if (!rows.length) return;
  // BigQuery DML allows multi-row VALUES in a single statement.
  const valClauses = rows.map(row =>
    '(' + row.map(v =>
      v === null     ? 'NULL'
      : typeof v === 'number' ? String(v)
      : `'${esc(v)}'`
    ).join(', ') + ')'
  ).join(',\n');
  await db.exec(
    `INSERT INTO ${table} (${cols.join(', ')}) VALUES\n${valClauses}`,
  );
}

// SQLite / Postgres: individual parameterized inserts.
async function paramInsertLoop(table, cols, rows) {
  if (!rows.length) return;
  const placeholders = cols.map(() => '?').join(', ');
  const sql = `INSERT INTO ${table} (${cols.join(', ')}) VALUES (${placeholders})`;
  for (const row of rows) {
    await db.run(sql, row);
  }
}
